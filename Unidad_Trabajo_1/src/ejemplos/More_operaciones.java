package ejemplos;

public class More_operaciones {

	public static void main(String[] args) {
		// Declaramos e inicializamos los valores
		int a=4, b=8;
		int res;
		
		//Realizamos las operaciones
		res=a%b;		// res=4
		res++;          // res=5
		res/=b;         // res=a/b=5/8=0
		res*=2;         // res=0
		
		//Mostramos los resultados en pantalla
		System.out.println(res);
	

	}

}
