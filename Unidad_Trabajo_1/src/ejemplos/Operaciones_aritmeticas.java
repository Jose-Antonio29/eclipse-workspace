package ejemplos;

public class Operaciones_aritmeticas {

	public static void main(String[] args) {
		// Declaración e inicialización de variables
		int a=5, b=3;
		
		// Operaciones aritméticas
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		double c=5;
		int d=2;
		
		System.out.println(c+d);
		System.out.println(c-d);
		System.out.println(c*d);
		System.out.println(c/d);
			
		
	}

}
