package ejemplos;

public class Operaciones_incrementales {

	public static void main(String[] args) {
		//Declaración
		int a;
		int b;
		
		// Inicialización
		a=5;
		b=++a;
		
		System.out.println(a);
		System.out.println(b);
		
		a=5;
		b=a++;
		
		System.out.println(a);
		System.out.println(b);

	}

}
