package ejemplos;

public class Variables {

	public static void main(String[] args) {
		int edad=19;                       // Variable de tipo entero (número sin decimales)
		String nombre="José Antonio";	   // Variable de tipo String (cadena de caracteres)
		double nota=7.25;                  // Variable de tipo real (número con decimales)
		
		System.out.println("Nombre: "+nombre);
		System.out.println("Edad: "+edad);
		System.out.println("Nota: "+nota);
		
		nota=nota-1;
		System.out.println("Nota: "+nota);
		
		

	}

}
