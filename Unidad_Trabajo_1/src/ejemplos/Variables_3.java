package ejemplos;

public class Variables_3 {

	public static void main(String[] args) {
		int num;       // Declaración (reserva espacio en la memoria)    
 		num=3;         // Inicialización (asigna un valor)
		// Se puede escribir directamente int num=3;
 		
 		num=num+10;
 		System.out.println(num);   //13
 		
 		num=num-5;
 		System.out.println(num);   //8
 		
 		System.out.println(num-3); //5
 		System.out.println(num);   //8
	}

}
