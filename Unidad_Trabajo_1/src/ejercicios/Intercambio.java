public class Intercambio {

	public static void main(String[] args) {
		// Declaramos e inicializamos las variables
		int a=5, b=10, var=0;
		
		// Realizamos el intercambio de valores
	    var=a;
	    a=b;
	    b=var;
		
	    // Realizamos el intercambio de valores
		System.out.println("a: "+a);
		System.out.println("b: "+b);

	}

}
