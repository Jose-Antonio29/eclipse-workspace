package ejemplos.bucles;

public class Contar1a10for {

	public static void main(String[] args) {
		// Contamos del 1 al 10 con un bucle for
		System.out.println("Contamos del 1 al 10: ");
		for(int conta=1; conta<=10; conta++) {
			System.out.print(conta+" ");
		}
		
		// Contamos del 10 al 1 con un bucle for
		System.out.println("\n\nContamos del 10 al 1: ");
		for(int conta=10; conta>0; conta--) {
			System.out.print(conta+" ");
		}

	}

}
